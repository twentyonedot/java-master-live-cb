package src.L1_Basics;

public class LoopDemo {
    public static void main(String[] args) {
        int count = 1;
        /* Increement(pre, post), Decreement(pre, post) Operators: '++', '--' */
        /* Loops - while */
        while (count <= 3) {
            System.out.println("Hello " + count);
            count++;
        }
    }
}
